# Iterating over a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Iterating over a string
for char in "hello":
    print(char)

# Using range()
for i in range(5):
    print(i)

for i in range (1,6):
    print(i)
    
s="Harry"
for i in s:
    print(i)
    
# for loop with else

l=[1,7,8]
for item in l:
    print(item)
else:
    print("done")
    
    